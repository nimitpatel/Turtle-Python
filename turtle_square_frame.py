import turtle

my_turtle=turtle.Turtle()

def square():
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
    my_turtle.left(90)
    my_turtle.forward(50)
square()

my_turtle.forward(100)
square()
my_turtle.forward(100)
square()
my_turtle.forward(100)
square()

my_turtle.forward(100)
